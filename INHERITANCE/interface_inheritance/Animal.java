package INHERITANCE.interface_inheritance;

/*
 * Una interfaz es una manera en la que clases se 
 * suscriben a determinados métodos
 * 
 * Cuando llamemos a la interfaz desde una clase
 * se verá de la manera siguiente:
 * Usando la palabra reservada "implements"
 * public class Dog implements Animal{
 * 
 * }
 */
public interface Animal {
    void sound();
}



